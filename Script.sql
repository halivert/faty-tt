CREATE TABLE MEDICO (
  ID_MEDICO INT AUTO_INCREMENT PRIMARY KEY,
  NOMBRE VARCHAR(50), 
  APELLIDO_PATERNO VARCHAR(50), 
  APELLIDO_MATERNO VARCHAR(50), 
  EMAIL VARCHAR(50), 
  KEYWORD BLOB, 
  FECHA_NACIMIENTO DATE, 
  CEDULA_PROFESIONAL VARCHAR(20),
  SEXO VARCHAR(50)
);

CREATE TABLE PACIENTE (
  ID_PACIENTE INT AUTO_INCREMENT PRIMARY KEY,
  ID_MEDICO INT,
  NOMBRE VARCHAR(50), 
  APELLIDO_PATERNO VARCHAR(50), 
  APELLIDO_MATERNO VARCHAR(50), 
  EMAIL VARCHAR(50), 
  KEYWORD BLOB, 
  FECHA_NACIMIENTO DATE, 
  SEXO VARCHAR(50),
  PESO DOUBLE(18, 2),
  TALLA DOUBLE(18, 2),
  ESTATURA DOUBLE(18, 2),
  IMC DOUBLE(18, 2),
  LIPIDOS DOUBLE(18, 2),
  CARBOHIDRATOS DOUBLE(18, 2),
  PROTEINAS DOUBLE(18, 2),

  CONSTRAINT FK_MEDICO_PACIENTE FOREIGN KEY(ID_MEDICO) REFERENCES MEDICO(ID_MEDICO)
);

CREATE TABLE TOKEN_MEDICO (
  ID_TOKEN_MEDICO INT AUTO_INCREMENT PRIMARY KEY,
  ID_MEDICO INT,
  TOKEN VARCHAR(10),

  CONSTRAINT FK_MEDICO_TOKEN_MEDICO FOREIGN KEY(ID_MEDICO) REFERENCES MEDICO(ID_MEDICO)
);